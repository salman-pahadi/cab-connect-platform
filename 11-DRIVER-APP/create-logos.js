const fs = require('fs');
const path = require('path');

// Create a simple PNG placeholder (1x1 green pixel, repeated)
const logosDir = path.join(__dirname, 'src', 'assets', 'logo');

if (!fs.existsSync(logosDir)) {
  fs.mkdirSync(logosDir, { recursive: true });
}

// Create minimal PNG files (1x1 pixel emerald green #10b981)
// PNG header + minimal IHDR chunk for 1x1 image
const minimalPNG = Buffer.from([
  // PNG signature
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
  // IHDR chunk (1x1 image)
  0x00, 0x00, 0x00, 0x0D,
  0x49, 0x48, 0x44, 0x52,
  0x00, 0x00, 0x00, 0x01,
  0x00, 0x00, 0x00, 0x01,
  0x08, 0x02, 0x00, 0x00, 0x00,
  0x90, 0x77, 0x53, 0xDE,
  // IDAT chunk (pixel data - RGB emerald green)
  0x00, 0x00, 0x00, 0x0C,
  0x49, 0x44, 0x41, 0x54,
  0x08, 0x99, 0x63, 0x10, 0xB9, 0x81, 0x00, 0x00,
  0x00, 0x00, 0x00, 0x01,
  0x9B, 0xB6, 0xEE, 0x56,
  // IEND chunk
  0x00, 0x00, 0x00, 0x00,
  0x49, 0x45, 0x4E, 0x44,
  0xAE, 0x42, 0x60, 0x82
]);

fs.writeFileSync(path.join(logosDir, 'logo-icon.png'), minimalPNG);
fs.writeFileSync(path.join(logosDir, 'logo-stacked.png'), minimalPNG);

console.log('✓ Created logo-icon.png');
console.log('✓ Created logo-stacked.png');
